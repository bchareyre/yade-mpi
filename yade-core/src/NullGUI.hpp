///////////////////////////////////////////////////////////////////////////////////////////////////
/// Methods											///
///////////////////////////////////////////////////////////////////////////////////////////////////

	public : int run(int argc, char *argv[]);

///////////////////////////////////////////////////////////////////////////////////////////////////
/// Internal Methods										///
///////////////////////////////////////////////////////////////////////////////////////////////////

	private	: void help();
	private	: int loop();

	private	:
		int 		interval;
		bool 		progress;
		int		snapshotInterval;
		string		snapshotName;
		long int	maxIteration;


	REGISTER_CLASS_NAME(NullGUI);
	REGISTER_BASE_CLASS_NAME(FrontEnd);

